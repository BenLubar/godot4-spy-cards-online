IconDef::IconDef() {
	texture.instantiate();
}
IconDef::~IconDef() {}

PackedByteArray IconDef::get_file_id() const {
	return file_id;
}
void IconDef::set_file_id(PackedByteArray new_file_id) {
	if (file_id != new_file_id) {
		file_id = new_file_id;
		emit_changed();
	}
}
Ref<Image> IconDef::get_image() const {
	return image;
}
void IconDef::set_image(Ref<Image> new_image) {
	if (image != new_image) {
		image = new_image;
		if (image.is_valid() && !image->has_mipmaps()) {
			image->generate_mipmaps();
		}
		texture->set_image(image);
		emit_changed();
	}
}
Ref<ImageTexture> IconDef::get_texture() const {
	return texture;
}

IconDef *IconDef::convert_legacy_portrait(Icon portrait, PackedByteArray data) {
	ERR_FAIL_COND_V_MSG(portrait != LEGACY_PORTRAIT_EMBEDDED && portrait != LEGACY_PORTRAIT_EXTERNAL, nullptr, "invalid portrait ID for legacy");

	if (portrait == LEGACY_PORTRAIT_EMBEDDED) {
		Ref<Image> image;
		image.instantiate();

		Error err = image->load_png_from_buffer(data);
		ERR_FAIL_COND_V_MSG(err != OK, nullptr, "failed to decode PNG");

		IconDef *def = memnew(IconDef);
		def->set_image(image);
		return def;
	}

	IconDef *def = memnew(IconDef);
	def->set_file_id(data);
	return def;
}
